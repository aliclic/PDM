class MainActivity : ComponentActivity() {
    override fun onCreate(savedInstanceState: Bundle?) {
        super.onCreate(savedInstanceState)
        enableEdgeToEdge()
        setContent {
            CardTheme {
                Scaffold(modifier = Modifier.fillMaxSize()) { innerPadding ->
                    Greeting(
                        name = "Android",
                        modifier = Modifier.padding(innerPadding)
                    )
                }
            }
        }
    }
}

@Composable
fun Greeting(name: String, modifier: Modifier = Modifier) {
    Text(
        text = "Hello $name!",
        modifier = modifier
    )
}

@Composable
fun CardExamples() {
    Column(
        modifier = Modifier
            .padding(16.dp)
            .fillMaxSize(),
        verticalArrangement = Arrangement.spacedBy(8.dp),
        horizontalAlignment = Alignment.CenterHorizontally,
    ) {
        ElevatedCardExample()
        FilledCardExample()
        OutlinedCardExample()
        CustomCardExample { Log.d("custom card", "hello word") }
    }
}

@OptIn(ExperimentalMaterial3Api::class)
// [START android_compose_components_customcard]
@Composable
fun CustomCardExample(event: () -> Unit) {
    Card(
        border = BorderStroke(1.dp, Color.Black),
        colors = CardDefaults.cardColors(
            containerColor = MaterialTheme.colorScheme.surfaceVariant,
        ),
        elevation = CardDefaults.cardElevation(
            defaultElevation = 4.dp
        ),
        enabled = true,
        modifier = Modifier.padding(16.dp),
        onClick = event,
        shape = RoundedCornerShape(8.dp),
    ) {
        Text(
            text = "Custom card that uses the Card composable. Tap me to say hello world.",
            modifier = Modifier
                .padding(16.dp)
                .fillMaxSize()
        )
    }
}
// [END android_compose_components_customcard]

